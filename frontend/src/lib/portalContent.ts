export type ProgramSlug = keyof typeof programDetails;

export function resolveProgramSlugFromName(programName: string | null | undefined): ProgramSlug | null {
  if (!programName) {
    return null;
  }

  if (programName.includes("يسير")) {
    return "yasir";
  }

  if (programName.includes("فرط")) {
    return "adhd";
  }

  return null;
}

